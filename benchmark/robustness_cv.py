"""Experiment 3: robustness under common image perturbations.

For each perturbation type x severity, top-1 accuracy is measured on
the robustness subset (generated by ``make_splits.py``). Works on the
un-normalised 224x224 tensor (range [0, 1]) so that PIL-based
operations (JPEG, rotation) match what a real camera pipeline produces.

Outputs under ``benchmark/report/``:

- ``robustness_table.csv``   : rows of (perturbation, severity, accuracy).
- ``robustness_curves.png``  : grid of accuracy-vs-severity curves.
- ``robustness_summary.json``: summary, including the most-sensitive perturbation.
"""

from __future__ import annotations

import argparse
import io
import json
from pathlib import Path
from typing import Callable, Dict, List, Tuple

import numpy as np
import torch
import torch.nn.functional as F
from PIL import Image
from torch.utils.data import DataLoader
from torchvision import transforms
from tqdm import tqdm

from benchmark.common import (
    DEFAULT_MODEL_PATH,
    IMAGENET_MEAN,
    IMAGENET_STD,
    ManifestDataset,
    SPLITS_DIR,
    ensure_report_dir,
    load_model,
    read_manifest,
)

PerturbFn = Callable[[torch.Tensor], torch.Tensor]


def _to_pil_list(batch: torch.Tensor) -> List[Image.Image]:
    # batch: (B,3,H,W) in [0,1]
    to_pil = transforms.ToPILImage()
    return [to_pil(x.clamp(0, 1)) for x in batch]


def _from_pil_list(pil_list: List[Image.Image]) -> torch.Tensor:
    to_tensor = transforms.ToTensor()
    return torch.stack([to_tensor(p) for p in pil_list])


def perturb_noise(batch: torch.Tensor, sigma: float) -> torch.Tensor:
    return (batch + torch.randn_like(batch) * sigma).clamp(0, 1)


def perturb_blur(batch: torch.Tensor, kernel: int) -> torch.Tensor:
    sigma = max(0.5, kernel / 3.0)
    tf = transforms.GaussianBlur(kernel_size=kernel, sigma=sigma)
    return tf(batch)


def perturb_jpeg(batch: torch.Tensor, quality: int) -> torch.Tensor:
    out = []
    for pil in _to_pil_list(batch):
        buf = io.BytesIO()
        pil.save(buf, format="JPEG", quality=int(quality))
        buf.seek(0)
        out.append(Image.open(buf).convert("RGB"))
    return _from_pil_list(out)


def perturb_brightness(batch: torch.Tensor, factor: float) -> torch.Tensor:
    return (batch * factor).clamp(0, 1)


def perturb_contrast(batch: torch.Tensor, factor: float) -> torch.Tensor:
    mean = batch.mean(dim=[2, 3], keepdim=True)
    return (mean + (batch - mean) * factor).clamp(0, 1)


def perturb_downsample(batch: torch.Tensor, size: int) -> torch.Tensor:
    small = F.interpolate(batch, size=size, mode="bilinear", align_corners=False)
    return F.interpolate(small, size=224, mode="bilinear", align_corners=False).clamp(0, 1)


def perturb_rotate(batch: torch.Tensor, degrees: float) -> torch.Tensor:
    # Use PIL to keep semantics consistent with camera rotation (fill=0).
    out = []
    for pil in _to_pil_list(batch):
        out.append(pil.rotate(degrees, resample=Image.BILINEAR))
    return _from_pil_list(out)


def perturb_occlusion(batch: torch.Tensor, ratio: float) -> torch.Tensor:
    result = batch.clone()
    _, _, h, w = result.shape
    bh = int(h * ratio)
    bw = int(w * ratio)
    y0 = (h - bh) // 2
    x0 = (w - bw) // 2
    result[:, :, y0:y0 + bh, x0:x0 + bw] = 0.0
    return result


PERTURBATIONS: Dict[str, List[Tuple[str, PerturbFn]]] = {
    "gaussian_noise": [
        ("sigma=0.02", lambda x: perturb_noise(x, 0.02)),
        ("sigma=0.05", lambda x: perturb_noise(x, 0.05)),
        ("sigma=0.10", lambda x: perturb_noise(x, 0.10)),
    ],
    "gaussian_blur": [
        ("k=3",  lambda x: perturb_blur(x, 3)),
        ("k=7",  lambda x: perturb_blur(x, 7)),
        ("k=11", lambda x: perturb_blur(x, 11)),
    ],
    "jpeg_compression": [
        ("q=70", lambda x: perturb_jpeg(x, 70)),
        ("q=40", lambda x: perturb_jpeg(x, 40)),
        ("q=20", lambda x: perturb_jpeg(x, 20)),
    ],
    "brightness": [
        ("f=0.6", lambda x: perturb_brightness(x, 0.6)),
        ("f=1.4", lambda x: perturb_brightness(x, 1.4)),
        ("f=1.8", lambda x: perturb_brightness(x, 1.8)),
    ],
    "contrast": [
        ("f=0.6", lambda x: perturb_contrast(x, 0.6)),
        ("f=1.4", lambda x: perturb_contrast(x, 1.4)),
        ("f=1.8", lambda x: perturb_contrast(x, 1.8)),
    ],
    "downsample": [
        ("128", lambda x: perturb_downsample(x, 128)),
        ("96",  lambda x: perturb_downsample(x, 96)),
        ("64",  lambda x: perturb_downsample(x, 64)),
    ],
    "rotation": [
        ("15deg", lambda x: perturb_rotate(x, 15)),
        ("30deg", lambda x: perturb_rotate(x, 30)),
        ("45deg", lambda x: perturb_rotate(x, 45)),
    ],
    "center_occlusion": [
        ("10%", lambda x: perturb_occlusion(x, 0.10)),
        ("20%", lambda x: perturb_occlusion(x, 0.20)),
        ("30%", lambda x: perturb_occlusion(x, 0.30)),
    ],
}


@torch.no_grad()
def eval_batches(
    model: torch.nn.Module,
    device: torch.device,
    raw_batches: List[torch.Tensor],
    label_batches: List[torch.Tensor],
    perturb: PerturbFn | None,
) -> float:
    mean = torch.tensor(IMAGENET_MEAN).view(1, 3, 1, 1)
    std = torch.tensor(IMAGENET_STD).view(1, 3, 1, 1)
    correct = 0
    total = 0
    for raw, labels in zip(raw_batches, label_batches):
        x = perturb(raw) if perturb is not None else raw
        x = (x - mean) / std
        x = x.to(device, non_blocking=True)
        logits = model(x)
        preds = logits.argmax(dim=1).cpu()
        correct += int((preds == labels).sum())
        total += int(labels.numel())
    return correct / total if total else 0.0


def cache_raw_batches(loader: DataLoader) -> tuple[List[torch.Tensor], List[torch.Tensor]]:
    """Pre-load raw tensors into memory. Robust subset is small (~5%)."""
    raws: List[torch.Tensor] = []
    labels: List[torch.Tensor] = []
    for _norm, raw, y, _paths in tqdm(loader, desc="cache raw"):
        raws.append(raw)
        labels.append(y.clone().detach() if torch.is_tensor(y) else torch.tensor(y))
    return raws, labels


def plot_curves(table: List[dict], out_path: Path) -> None:
    import matplotlib.pyplot as plt

    # Group rows by perturbation; severity index on x-axis.
    groups: Dict[str, List[dict]] = {}
    for r in table:
        if r["perturbation"] == "baseline":
            continue
        groups.setdefault(r["perturbation"], []).append(r)

    baseline_acc = next((r["accuracy"] for r in table if r["perturbation"] == "baseline"), None)

    n = len(groups)
    cols = 4
    rows = (n + cols - 1) // cols
    fig, axes = plt.subplots(rows, cols, figsize=(4.2 * cols, 3.0 * rows), sharey=True)
    axes = np.array(axes).reshape(-1)
    for i, (name, entries) in enumerate(groups.items()):
        ax = axes[i]
        xs = list(range(1, len(entries) + 1))
        ys = [e["accuracy"] for e in entries]
        ax.plot(xs, ys, "o-", color="#e76f51")
        if baseline_acc is not None:
            ax.axhline(baseline_acc, color="#264653", linestyle="--", label=f"baseline={baseline_acc:.3f}")
        ax.set_xticks(xs)
        ax.set_xticklabels([e["severity"] for e in entries], rotation=25, fontsize=8)
        ax.set_title(name)
        ax.set_ylim(0, 1.02)
        ax.grid(alpha=0.3)
        if i == 0:
            ax.set_ylabel("Top-1 accuracy")
        ax.legend(fontsize=7)

    for j in range(i + 1, len(axes)):
        axes[j].axis("off")

    fig.suptitle("Robustness: accuracy vs perturbation severity", y=1.02)
    fig.tight_layout()
    fig.savefig(out_path, dpi=150, bbox_inches="tight")
    plt.close(fig)


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--manifest", type=Path, default=SPLITS_DIR / "robust_manifest.csv")
    parser.add_argument("--model", type=Path, default=DEFAULT_MODEL_PATH)
    parser.add_argument("--batch-size", type=int, default=16)
    parser.add_argument("--num-workers", type=int, default=2)
    parser.add_argument("--device", type=str, default=None)
    parser.add_argument("--seed", type=int, default=2026)
    args = parser.parse_args()

    torch.manual_seed(args.seed)
    np.random.seed(args.seed)

    device = torch.device(args.device) if args.device else None
    model, class_names, device = load_model(args.model, device=device)
    print(f"[robustness] device={device}")

    rows = read_manifest(args.manifest)
    ds = ManifestDataset(rows, return_raw=True)
    loader = DataLoader(ds, batch_size=args.batch_size, shuffle=False,
                        num_workers=args.num_workers,
                        pin_memory=(device.type == "cuda"))

    raws, label_batches = cache_raw_batches(loader)

    table: List[dict] = []
    baseline_acc = eval_batches(model, device, raws, label_batches, perturb=None)
    print(f"[robustness] baseline accuracy on robust subset: {baseline_acc:.4f}")
    table.append({"perturbation": "baseline", "severity": "-", "accuracy": baseline_acc})

    for pert_name, severities in PERTURBATIONS.items():
        for sev_name, fn in severities:
            acc = eval_batches(model, device, raws, label_batches, perturb=fn)
            print(f"[robustness] {pert_name:18s} {sev_name:10s} acc={acc:.4f}")
            table.append({"perturbation": pert_name, "severity": sev_name, "accuracy": acc})

    report_dir = ensure_report_dir()
    csv_path = report_dir / "robustness_table.csv"
    with csv_path.open("w", encoding="utf-8") as f:
        f.write("perturbation,severity,accuracy\n")
        for r in table:
            f.write(f"{r['perturbation']},{r['severity']},{r['accuracy']:.4f}\n")

    plot_curves(table, report_dir / "robustness_curves.png")

    # Average accuracy per perturbation for a quick ranking.
    per_pert_avg = {}
    for r in table:
        if r["perturbation"] == "baseline":
            continue
        per_pert_avg.setdefault(r["perturbation"], []).append(r["accuracy"])
    per_pert_avg = {k: float(np.mean(v)) for k, v in per_pert_avg.items()}
    most_sensitive = min(per_pert_avg, key=per_pert_avg.get) if per_pert_avg else None
    least_sensitive = max(per_pert_avg, key=per_pert_avg.get) if per_pert_avg else None

    summary = {
        "n_samples": int(sum(t.numel() for t in label_batches)),
        "baseline_accuracy": baseline_acc,
        "per_perturbation_mean_accuracy": per_pert_avg,
        "most_sensitive": most_sensitive,
        "least_sensitive": least_sensitive,
        "table": table,
    }
    (report_dir / "robustness_summary.json").write_text(
        json.dumps(summary, indent=2, ensure_ascii=False), encoding="utf-8"
    )
    print(f"[robustness] most sensitive: {most_sensitive}, least sensitive: {least_sensitive}")


if __name__ == "__main__":
    main()
